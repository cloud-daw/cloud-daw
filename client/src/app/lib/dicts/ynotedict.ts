export function createYNoteDict() {
    const notePositions = {};
    const octaves = [7, 6, 5, 4, 3, 2, 1, 0];
    
    const noteOrder = ['C', 'C#', 'D', 'D#', 'E', 'F', 'F#', 'G', 'G#', 'A', 'A#', 'B'];

    let noteIndex = 0;
    octaves.forEach(octave => {
        noteOrder.forEach(note => {
          const noteName = `${note}${octave}`;
          const topPos = noteIndex * 100 / 95;
          notePositions[noteName] = topPos;
          noteIndex++;
        });
      });
    console.log('created Note Dict', notePositions);
    return notePositions;
}

